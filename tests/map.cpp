void print() {
    for (int i = 0; i < 20; i++) {
        for (int j = 0; j < 20; j++) {
            if (map[j][i] == 1) {
                Console::SetCursorPosition(i, j);
                Console::ForegroundColor = ConsoleColor::White;
                cout << char(219);
            }
        }
    }
}

void player() {
    char key;
    int x = 9, y = 9;

    Console::SetCursorPosition(x, y);
    cout << '*';

    while (true) {
        key = _getch();
        Console::SetCursorPosition(x, y);
        cout << " ";
        if (key == 72 && map[y - 1][x] != 1) {
            y--;
            if (y < 0) y++;
        }
        if (key == 80 && map[y + 1][x] != 1) {
            y++;
            if (y > 20) y--;
        }
        if (key == 75 && map[y][x - 1] != 1) {
            x--;
            if (x < 0) x++;
        }
        if (key == 77 && map[y][x + 1] != 1) {
            x++;
            if (x > 20) x--;
        }
        Console::SetCursorPosition(x, y);
        cout << '*';
    }
}

